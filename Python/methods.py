# Upper Method(this will uppercase all the characters)
x='Hello World'
print('the uppered string=',x.upper())        
# Lower Method
print('the lowered string=',x.lower())

# Split Method( this will split the string on the basis of whitespace or special character provided)
print('the splitted string=',x.split()) # by default split method will split on the basis of whitespace
a='this is @Awesome'
print("splitted string from '@'=",a.split('@'))  # this will split on the basis of '@'
print("splitted string from 's'=",a.split('s')) 